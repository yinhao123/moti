<script>
import { addCar, getCar, subCar, subCarNum, syncBuyCar } from '../../common/request.js'
import { setCartCache, getCartCache } from '../../common/utils.js'
	export default {
		data() {
			return {
				isEmpty: false,
				sumPrice: '0.00',
				headerPosition: "fixed",
				headerTop: null,
				statusTop: null,
				selectedList: [],
				isAllselected: false,
				goodsList: [],
				//控制滑动效果
				theIndex: null,
				oldIndex: null,
				isStop: false
			}
		},
		onPageScroll(e) {
			//兼容iOS端下拉时顶部漂移
			this.headerPosition = e.scrollTop >= 0 ? "fixed" : "absolute";
			this.headerTop = e.scrollTop >= 0 ? null : 0;
			this.statusTop = e.scrollTop >= 0 ? null : -this.statusHeight + 'px';
		},
		//下拉刷新，需要自己在page.json文件中配置开启页面下拉刷新 "enablePullDownRefresh": true
		onPullDownRefresh() {
			setTimeout(function() {
				uni.stopPullDownRefresh();
			}, 1000);
		},
		onLoad() {
			//兼容H5下结算条位置
			// #ifdef H5
			this.footerbottom = document.getElementsByTagName('uni-tabbar')[0].offsetHeight + 'px';
			// #endif
			// #ifdef APP-PLUS
			this.statusHeight = plus.navigator.getStatusbarHeight();
			// #endif
		},
		onShow() { // 页面每次显示，就更新同步一次最新的购物车信息
			this.getCartData()
			
		},
		methods: {
			goBuy() {
				uni.switchTab({
					url: '/pages/index/index'
				})
			},
			async getCartData() { // 获取购物车信息
				let {data} = await getCar()
				if (data.code === "0") {
					// this.goodsList = data.result
					this.goodsList = [{
						id: 1,
						img: '../../static/22.png',
						name: 'MOTI雾化弹 补充替换装（相同口味4颗）',
						spec: '经典烟草4支装',
						price: 127.5,
						number: 3,
						selected: false
					},
					{
						id: 4,
						img: '../../static/54.png',
						name: 'MOTI雾化弹 补充替换装（相同口味4颗）',
						spec: '(新品)绿豆冰沙4支装',
						price: 127.5,
						number: 8,
						selected: false
					},
					{
						id: 3,
						img: '../../static/77.png',
						name: 'MOTI雾化弹 补充替换装（相同口味4颗）',
						spec: '(新品)绿豆冰沙4支装',
						price: 127.5,
						number: 6,
						selected: false
					}
				]
				}
			},
			//加入商品 参数 goods:商品数据
			joinGoods(goods) {
				/*
				 * 这里只是展示一种添加逻辑，模板并没有做从其他页面加入商品到购物车的具体动作，
				 * 在实际应用上，前端并不会直接插入记录到goodsList这一个动作，一般是更新列表和本地列表缓存。
				 * 一般商城购物车的增删改动作是由后端来完成的,
				 * 后端记录后返回前端更新前端缓存
				 */
				let len = this.goodsList.length;
				let isFind = false; //是否找到ID一样的商品
				for (let i = 0; i < len; i++) {
					let row = this.goodsList[i];
					if (row.id == goods.id) { //找到商品一样的商品
						this.goodsList[i].number++; //数量自增
						isFind = true; //找到一样的商品
						break; //跳出循环
					}
				}
				if (!isFind) {
					//没有找到一样的商品，新增一行到购物车商品列表头部
					this.goodsList[i].unshift(goods);
				}
			},
			//控制左滑删除效果-begin
			touchStart(index, event) {
				//多点触控不触发
				if (event.touches.length > 1) {
					this.isStop = true;
					return;
				}
				this.oldIndex = this.theIndex;
				this.theIndex = null;
				//初始坐标
				this.initXY = [event.touches[0].pageX, event.touches[0].pageY];
			},
			touchMove(index, event) {
				//多点触控不触发
				if (event.touches.length > 1) {
					this.isStop = true;
					return;
				}
				let moveX = event.touches[0].pageX - this.initXY[0];
				let moveY = event.touches[0].pageY - this.initXY[1];

				if (this.isStop || Math.abs(moveX) < 5) {
					return;
				}
				if (Math.abs(moveY) > Math.abs(moveX)) {
					// 竖向滑动-不触发左滑效果
					this.isStop = true;
					return;
				}

				if (moveX < 0) {
					this.theIndex = index;
					this.isStop = true;
				} else if (moveX > 0) {
					if (this.theIndex != null && this.oldIndex == this.theIndex) {
						this.oldIndex = index;
						this.theIndex = null;
						this.isStop = true;
						setTimeout(() => {
							this.oldIndex = null;
						}, 150)
					}
				}
			},
			touchEnd(index, $event) {
				//结束禁止触发效果
				this.isStop = false;
			},
			//控制左滑删除效果-end
			
			//商品跳转
			toGoods(goodsId) {
				uni.showToast({
					title: '商品' + goodsId,
					icon: "none"
				});
				uni.navigateTo({
					url: '../goods/goods?goodsId=' + goodsId
				});
			},
			//跳转确认订单页面
			toConfirmation() {
				let tmpList = [];
				let len = this.goodsList.length;
				for (let i = 0; i < len; i++) {
					if (this.goodsList[i].selected) {
						tmpList.push(this.goodsList[i]);
					}
				}
				if (tmpList.length < 1) {
					uni.showToast({
						title: '请选择商品结算',
						icon: 'none'
					});
					return;
				}
				uni.setStorage({
					key: 'buylist',
					data: tmpList,
					success: () => {
						uni.navigateTo({
							url: '../placeOrder/placeOrder'
						})
					}
				})
			},
			//删除单个商品
			async deleteGoods(id) {
				
				let {data} = await subCar(id) // 这里接口有问题，不能传一个数组
				data.code = "0"
				if (data.code !== "0") { // 若后台联动失败，那么就阻止前台更改
					this.showTips(data.msg)
					return false
				}
				
				let len = this.goodsList.length;
				for (let i = 0; i < len; i++) {
					if (id == this.goodsList[i].id) {
						this.goodsList.splice(i, 1);
						break;
					}
				}
				// this.selectedList.splice(this.selectedList.indexOf(id), 1);
				this.sum();
				this.oldIndex = null;
				this.theIndex = null;
			},
			// 循环删除多个商品s
			deleteList() {
				let selectedList = this.selectedList
				let len = selectedList.length;
				while (selectedList.length > 0) {
					this.deleteGoods(selectedList[0])
					selectedList.splice(selectedList.indexOf(selectedList[0]), 1) // 删除数组长度不能放到异步请求里去执行，否则会因为while一直循环不完，异步不能执行去减数组长度，出现死循环
				}
				selectedList = [];
				this.isAllselected = selectedList.length == this.goodsList.length && this.goodsList.length > 0;
				this.sum();
			},
			// 选中商品
			selected(index) {
				this.goodsList[index].selected = this.goodsList[index].selected ? false : true;
				let i = this.selectedList.indexOf(this.goodsList[index].id);
				i > -1 ? this.selectedList.splice(i, 1) : this.selectedList.push(this.goodsList[index].id);
				this.isAllselected = this.selectedList.length == this.goodsList.length;
				this.sum();
			},
			//全选
			allSelect() {
				let len = this.goodsList.length;
				let arr = [];
				for (let i = 0; i < len; i++) {
					this.goodsList[i].selected = this.isAllselected ? false : true;
					arr.push(this.goodsList[i].id);
				}
				this.selectedList = this.isAllselected ? [] : arr;
				this.isAllselected = this.isAllselected || this.goodsList.length == 0 ? false : true;
				this.sum();
			},
			// 减少数量
			async sub(index, goodsId) {
				let goods = this.goodsList[index]
				if (goods.number <= 1) return
				
				let {data} = await subCarNum(goodsId, goods.number - 1)
				data.code = "0"
				if (data.code === "0") { // 减少数量成功
					goods.number --
					this.sum()
				}
			},
			// 增加数量
			async add(index, goodsId) {
				let goods = this.goodsList[index]
				
				let {data} = await addCar(goodsId, goods.number - 0 + 1)
				data.code = "0"
				if (data.code === "0") { // 增加数量成功
					goods.number ++
					this.sum()
				}
			},
			// 合计
			async sum(e, index, goodsId) {
				if (goodsId) {
					let {data} = await subCarNum(goodsId, e.detail.value) 
					data.code = "0"
					if (data.code !== "0") { // 若后台没有联动成功，那么就禁止前台页面操作
						this.showTips(data.msg)
						return false
					}
				}
				
				this.sumPrice = 0;
				let len = this.goodsList.length;
				for (var i = 0; i < len; i++) {
					if (this.goodsList[i].selected) {
						if (e && i == index) {
							this.sumPrice = this.sumPrice + (e.detail.value * this.goodsList[i].price);
						} else {
							this.sumPrice = this.sumPrice + (this.goodsList[i].number * this.goodsList[i].price);
						}
					}
				}
				this.sumPrice = this.sumPrice.toFixed(2);
			},
			discard() {
				//丢弃
			},
			showTips(title) { // 弹窗
				uni.showToast({
					title,
					icon: 'none'
				})
			}
		}
	}
</script>

